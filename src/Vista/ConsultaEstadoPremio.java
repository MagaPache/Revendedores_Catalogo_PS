    private void loadCmbOfficialAgent(ArrayList<AgenteOficial> getOfficialAgents) {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for (AgenteOficial officialAgent : getOfficialAgents) {
            model.addElement(officialAgent);
        }
        cmbOfficialAgent.setModel(model);
    }

    private void loadTableActivePrices() {
        DefaultTableModel model = new DefaultTableModel();
        String[] columns = {"Código", "Premio", "Fecha Comienzo", "Fecha Límite", "Condición Categoría Producto", "Cantidad Condición"};
        model.setColumnIdentifiers(columns);
        for (VmPremioCondicion price : prices) {
            Object[] rows = {price.getIdPrice(), price.getPriceName(), price.getStartDate(), price.getLimitDate(), price.getProductCategory(), price.getAmount()};
            model.addRow(rows);
        }
        tblActivePrices.setModel(model);
    }

